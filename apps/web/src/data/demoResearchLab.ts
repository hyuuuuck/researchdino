function deputyAssignment(
  deputy: LaboratoryRoomData["agent"],
  label: string,
  responsibility: string,
  provider: "ollama" | "claude",
  model: string,
  modelRef: string,
  mode: "primary" | "cross_check" | "fallback" | "tool" = "primary",
  local = false,
) {
  return {
    id: `${deputy}-${mode}-${provider}`,
    deputy,
    label,
    provider,
    model,
    modelRef,
    mode,
    responsibility,
    local,
  };
}

function ollamaDeputy(
  deputy: LaboratoryRoomData["agent"],
  label: string,
  responsibility: string,
  mode: "primary" | "cross_check" | "fallback" | "tool" = "primary",
) {
  const model = ollamaRoleModels[deputy];
  return deputyAssignment(
    deputy,
    label,
    responsibility,
    "ollama",
    model,
    `ollama-cloud:${model}`,
    mode,
    false,
  );
}

